/*
 * Muestra los números múltiplos de 5 de 0 a 100 utilizando un bucle while .
 */
package Tema5;

/**
 *
 * @author angelo
 */
public class Ejercicio2 {
    public static void main(String[] args) {
        
        int i=0;
        while(i<100){
            
            System.out.print(i+", ");
            i+=5;
        }
    }
   
}
